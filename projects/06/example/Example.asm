@R0 //לשלוף את הערך מהאוגר הראשון
D=M //D הערך נכנס לאוגר
@n
M=D //n נשים את הערך בתוך אוגר שיהווה המשתנה
@sum
M=0 //נאפס את האוגר שיהווה המונה שלנו 
@i
M=1
(LOOP) //תחילת לולאה
	@i //iנבדוק מהו
	D=M 
	@n //n נבדוק האם כבר הגענו ל
	D=D-M 
	@STOP //לאיפה קופצים אם באמת סיימנו
	D;JGT //קפוץ n עקף את i אם באמ
	@sum //קדם את הסכום
	D=M //שים באוגר את הערך הקודם
	@i //?i מהו
	D=D+M //הוסף אותו לסכום שהיה
	@sum //שמור את הסכום החדש
	M=D
	@i //קדם את המונה באחד
	M=M+1
	@LOOP //הקפיצה תהיה לתווית LOOP
	0;JMP
(STOP)
	@sum //משוך את הסכום מהאוגר
	D=M
	@R1 //שים אותו באוגר שהוגדר להכיל את התוצאה
	M=D
(END)
	@END //לאיפה לקפוץ
	0;JMP // בלי תנאי. לולאה אינסופית